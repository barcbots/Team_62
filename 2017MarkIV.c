task usercontrol() {
	while(true) {
		tankDrive(vexRT[Ch3], vexRT[Ch2], 10);

		if(vexRT[Btn8U])
			lift(-127);
		else if(vexRT[Btn5U])
			lift(127);
		else if(vexRT[Btn5D] && !SensorValue[liftStop]) {
			if(SensorValue[liftPot]<1450)
				lift(-127);
			else if(SensorValue[liftPot]>2400)
				lift(-40);
			else
				lift(-80);
		}
		else if(SensorValue[liftStop]) //hold down code
			lift(-15);
		else if(SensorValue[liftPot]<1300)
			lift(-20);
		else if(SensorValue[liftPot]>2200)
			lift(15);
		else
			lift(0);

		if(vexRT[Btn6U]) {
			SensorValue[claw] = !SensorValue[claw];
			while(vexRT[Btn6U]) { delay(5); }
		}

	}
}
